public class LoanUser {
    
    String name;
    String surname;
    int age;
    int annualSalary;

    public LoanUser (String name, String surname, int age, int annualSalary){
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.annualSalary = annualSalary;
    }
}
